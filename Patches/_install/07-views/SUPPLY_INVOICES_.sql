IF OBJECT_ID('SUPPLY_INVOICES') IS NOT NULL DROP VIEW SUPPLY_INVOICES
GO
-- SELECT TOP 50 * FROM SUPPLY_INVOICES
CREATE VIEW SUPPLY_INVOICES
AS
SELECT    
    X.DOC_ID,    
    X.TYPE_ID,
    X.ACC_REGISTER_ID,  
    X.SUBJECT_ID,
    SUBJECT_NAME = SUBJ.SHORT_NAME,
    X.STATUS_ID,
    STATUS_NAME = S.NAME,
    X.D_DOC,
    X.D_DELIVERY,
    D_READY = X.D_ISSUE,
    X.NUMBER, 
    X.DOGOVOR_NUMBER,
    X.DOGOVOR_DATE,
    X.SPEC_NUMBER,
    X.SPEC_DATE,
    X.AGENT_ID,
    AGENT_NAME = AG.NAME,
    X.MOL_ID,
    MOL_NAME = M.NAME,
    X.CCY_ID,
    X.VALUE_CCY,
    X.VALUE_RUR,
    X.NOTE,
    X.CONTENT,        
    -- TIMESTAMP
    X.ADD_MOL_ID,
    X.ADD_DATE,
    X.UPDATE_DATE,
    X.UPDATE_MOL_ID,
    -- EXCHANGE
    X.EXTERN_ID,    
    X.SOURCE_ID,
    X.REPLICATE_DATE
FROM SDOCS X    
    JOIN SDOCS_STATUSES S ON S.STATUS_ID = X.STATUS_ID
    LEFT JOIN SUBJECTS SUBJ ON SUBJ.SUBJECT_ID = X.SUBJECT_ID
    LEFT JOIN AGENTS AG ON AG.AGENT_ID = X.AGENT_ID
    LEFT JOIN MOLS M ON M.MOL_ID = X.MOL_ID
WHERE X.TYPE_ID = 8
GO
